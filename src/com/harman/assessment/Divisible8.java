package com.harman.assessment;

public class Divisible8 {
    public int Divisibleby8(int a) {
        if (a % 8 == 0) {
            System.out.println("Given number is divisible by 8");
        } else {
            System.out.println("Given number is not divisible by 8");
        }
        return a;

    }
}
